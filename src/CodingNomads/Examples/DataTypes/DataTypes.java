package CodingNomads.Examples.DataTypes;

public class DataTypes {

    // Primitive data types

    // can only hold true or false
    boolean isAwesome = true;

    // 8 bits or one byte
    byte byteNum = 12;

    // 16 bits or two byte in memory
    short shortNum = 30000;

    // 32 bits of memory
    int intNum = 12342144;

    // 64 bits of memory
    long longNum = 342384239849342l;

    //32 bits of memory
    float floatNum = 54.342f;

    // 64 bits of memory
    double doubleNum = 124.54;

    // Stands for character and can be incremented
    char random = 'A';









}
